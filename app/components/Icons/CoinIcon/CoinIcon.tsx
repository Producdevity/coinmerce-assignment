export type CoinSymbol = keyof typeof icons | string

interface Props {
  symbol: CoinSymbol
}

async function importIcon(coinSymbol: string): Promise<string | null> {
  try {
    const module = await import(`~/assets/images/coins/30x30/${coinSymbol}.png`)
    return module.default as string
  } catch (error) {
    console.error(`Failed to load icon for ${coinSymbol}:`, error)
    return null
  }
}

function CoinIcon(props: Props) {
  const { iconSrc, isLoading, error } = useImportIcon(props.symbol)

  // useEffect(() => {
  //   importIcon(props.symbol).then(setIconSrc)
  // }, [props.symbol])

  if (isLoading)
    return (
      <div className="h-[30px] w-[30px]" role="status">
        <Spinner />
        <span className="sr-only">Loading...</span>
      </div>
    )

  if (error) {
  }
  return iconSrc ? (
    <img
      loading="lazy"
      className="h-[30px] w-[30px] rounded-full shadow-sm"
      src={iconSrc}
      alt={props.symbol}
    />
  ) : (
    <div className="h-[30px] w-[30px] rounded-full bg-gray-400 shadow-sm" />
  )
}

export default CoinIcon
